function toggleDegreeOptions(isDegreeProgram) {
  var degreeOptions = document.getElementById("degreeOptions");

  if (isDegreeProgram) {
    degreeOptions.style.display = "block";
  } else {
    degreeOptions.style.display = "none";
  }
}
